<template>
    <div class="flex flex-col justify-center items-center">
        <input type="text" class="w-full m-auto outline-none mt-20 mx-8 border border-black" v-model="characterGender">
        <button class="bg-green-400 w-48 mt-4 mx-20" @click="getCharByGender(characterGender)">Filter</button>
        {{ gender }}
    </div>
</template>

<script>
import { mapActions, mapGetters } from 'vuex';

export default {
    name: 'FilterByGender',
    data () {
        return {
            characterGender: null
        }
    },
    methods: {
        ...mapActions({ getCharByGender: 'getCharacterByGender' })
    },
    computed: {
        ...mapGetters({ gender: 'getGender' })
    }
}
</script>